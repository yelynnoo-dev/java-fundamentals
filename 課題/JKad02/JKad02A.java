public class JKad02A{
	public static void main(String[] args){
		int apple = 20;  // リンゴの数
		int person = 3;   // 人間の数 
		int ate=20/3;
		int left=20%3;
		
		//リンゴ数を表示します！
		System.out.println("リンゴが20個ありました！");
		System.out.println(apple);
		//人数を表示します！
		System.out.println("人間が3人やってきました！");
		System.out.println(person);
		//それぞれが食べたリンゴの数を表示します！
		System.out.println("それぞれが食べたリンゴの数は ");
		System.out.println(ate);
		//残ったリンゴの数を表示します！
		System.out.println("残ったリンゴの数は ");
		System.out.println(left);
	}
}